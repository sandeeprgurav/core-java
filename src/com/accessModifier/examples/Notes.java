	/*
	 * https://www.scientecheasy.com/2018/05/access-modifiers-interview-question-
	 * answer.html
	 * 
	 * a) Private: Private members of a class can be accessible only within the
	 * class. It cannot be accessed from outside the class. b) Default: Default
	 * members of a class are accessible within the same package due to visible only
	 * within the package. They cannot be accessed from outside the package.
	 * c)Protected: Protected members of a class are visible within the package.
	 * Therefore, we can only access within the package but can be accessed to the
	 * subclasses outside the package through the inheritance only. But not variable which is marked protected without inheritance. d) Public:Public
	 * members are visible anywhere. So, we can access anywhere within or outside
	 * the package.
	 * 
	 * Can we have a private constructor in Java? Yes, we can have a private
	 * constructor in Java. 1. The private constructor is used when you do not want
	 * to create the object of that class. 2. We cannot create a subclass of that
	 * class. 3. It is also used in singleton design pattern, Factory method design
	 * pattern.
	 * 
	 * Which access specifier can be used with a class? Public and Default access
	 * specifiers can be used with a class.
	 * 
	 * There are four non-access modifiers in Java. They are as follows: a. Static:
	 * This modifier is used to check that a member is a class member or instance
	 * member. If you declare a class as static, this class will be executed first.
	 * b. Final: Final is a keyword which is used to restrict the users. In other
	 * words, It is used to restrict further modification of a class, field or
	 * method. If a class is declared as 'final', the class cannot be subclassed. c.
	 * Abstract: Abstract is a keyword that is used with a class or a method. An
	 * abstract class or abstract method is used for further modification. If a
	 * class is declared as 'abstract', the class cannot be instantiated. d.
	 * Synchronization: It is used to achieve the thread safeness. Only one thread
	 * can enter in a synchronized method or block at a given time.
	 */
